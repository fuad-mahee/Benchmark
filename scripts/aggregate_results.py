"""Aggregate all per-step results into thesis-ready files:

  results/benchmark_report.json  - every metric from every step, machine-readable
  results/benchmark_report.md    - the five thesis tables, pre-formatted markdown

  python scripts/aggregate_results.py
Safe to run at any time; missing steps are simply omitted.
"""
import json
import sys
from pathlib import Path

sys.path.insert(0, str(Path(__file__).resolve().parents[1]))

from src.common.config import RESULTS_DIR, setup_env
from src.common.io_utils import run_metadata, save_json

PAPER_CLAIMS = {
    "glitch_counts_llama2": {"glitchprober": 6425, "glitchcleaner": 4743},
    "gp_detection": {"precision": 1.0, "recall_avg": 0.6447, "f1_avg": 0.7835},
    "gp_repair_rate_avg": 0.5006,
    "gp_rule_based_repair_rate_avg": 0.3679,
    "gc_repair_rate_avg": 0.8688,
    "speed_tok_s": {"base": 66.30, "glitchcleaner": 62.83, "glitchprober": 11.82},
}


def read_json(path: Path):
    if path.exists():
        with open(path, encoding="utf-8") as f:
            return json.load(f)
    return None


def collect(models: list[str]) -> dict:
    report = {"paper_claims": PAPER_CLAIMS, "models": {}}
    for m in models:
        entry = {}
        gt = read_json(RESULTS_DIR / "ground_truth" / m / "summary.json")
        if gt:
            entry["rq1_ground_truth"] = {"counts": gt.get("counts"),
                                         "sweep_seconds": gt.get("sweep_seconds")}
        det = read_json(RESULTS_DIR / "gp_detect" / m / "summary.json")
        if det:
            entry["rq2_gp_detection"] = {"mean": det.get("mean"), "std": det.get("std")}
        rep = read_json(RESULTS_DIR / "gp_repair" / m / "summary.json")
        if rep:
            entry["rq3_gp_repair"] = rep.get("results")
        gc_eval = read_json(RESULTS_DIR / "gc" / m / "eval.json")
        if gc_eval:
            entry["rq4_gc_repair"] = gc_eval.get("results")
        speed = read_json(RESULTS_DIR / "side_effects" / m / "speed.json")
        if speed:
            entry["rq5_speed"] = {"tokens_per_second": speed.get("tokens_per_second"),
                                  "relative": speed.get("relative")}
        if entry:
            report["models"][m] = entry
    return report


def fmt(x, pct=False):
    if x is None:
        return "-"
    return f"{x * 100:.2f}%" if pct else f"{x:.3f}" if isinstance(x, float) else str(x)


def to_markdown(report: dict) -> str:
    lines = ["# Benchmark Report", "",
             "Auto-generated by scripts/aggregate_results.py - do not edit by hand.", ""]
    models = report["models"]

    lines += ["## Table 1 - RQ1: Glitch-token census", "",
              "| Model | Candidates swept | Glitch | Normal | Filtered (special/undecodable/unreachable) |",
              "|---|---|---|---|---|"]
    for m, e in models.items():
        c = (e.get("rq1_ground_truth") or {}).get("counts") or {}
        filtered = sum(c.get(k, 0) for k in ("special", "undecodable", "unreachable"))
        swept = c.get("glitch", 0) + c.get("normal", 0)
        lines.append(f"| {m} | {swept} | {c.get('glitch', '-')} | {c.get('normal', '-')} | {filtered} |")
    lines += ["", "Paper reference (Llama-2): GlitchProber 6,425 / GlitchCleaner 4,743.", ""]

    lines += ["## Table 2 - RQ2: GlitchProber detection (mean over seeds)", "",
              "| Model | Precision | Recall | F1 | Time (s) |", "|---|---|---|---|---|"]
    for m, e in models.items():
        d = (e.get("rq2_gp_detection") or {}).get("mean") or {}
        lines.append(f"| {m} | {fmt(d.get('precision'))} | {fmt(d.get('recall'))} "
                     f"| {fmt(d.get('f1'))} | {fmt(d.get('time_seconds'))} |")
    lines += ["", "Paper claims: precision 100%, avg recall 64.47%, avg F1 0.7835.", ""]

    lines += ["## Table 3 - RQ3: GlitchProber repair", "",
              "| Model | Mode | Repair rate | Normal tokens broken |", "|---|---|---|---|"]
    for m, e in models.items():
        for mode, r in (e.get("rq3_gp_repair") or {}).items():
            lines.append(f"| {m} | {mode} | {fmt(r.get('repair_rate'), pct=True)} "
                         f"| {fmt(r.get('normal_break_rate'), pct=True)} |")
    lines += ["", "Paper claims: adaptive 50.06% avg, rule-based 36.79% avg. "
              "Collateral breakage is not reported in the paper.", ""]

    lines += ["## Table 4 - RQ4: GlitchCleaner repair (train vs HELD-OUT)", "",
              "| Model | Train-split repair | Held-out repair | Normal ok (adapter on) | Held-out repair (adapter off) |",
              "|---|---|---|---|---|"]
    for m, e in models.items():
        r = e.get("rq4_gc_repair") or {}
        lines.append(f"| {m} | {fmt(r.get('train_repair_rate'), pct=True)} "
                     f"| {fmt(r.get('heldout_repair_rate'), pct=True)} "
                     f"| {fmt(r.get('normal_ok_rate_adapter_on'), pct=True)} "
                     f"| {fmt(r.get('heldout_repair_rate_adapter_off'), pct=True)} |")
    lines += ["", "Paper claims 86.88% avg but (apparently) evaluates on its own training tokens; "
              "the held-out column is the generalization test.", ""]

    lines += ["## Table 5 - RQ5: Inference speed", "",
              "| Model | Variant | tok/s | Relative to base |", "|---|---|---|---|"]
    for m, e in models.items():
        s = e.get("rq5_speed") or {}
        for variant, v in (s.get("tokens_per_second") or {}).items():
            rel = (s.get("relative") or {}).get(variant)
            lines.append(f"| {m} | {variant} | {fmt(v)} | {fmt(rel)} |")
    lines += ["", "Paper (GlitchCleaner Table 6) claims: base 66.30, GC 62.83, GP 11.82 tok/s.", ""]
    return "\n".join(lines)


def main():
    cfg = setup_env()
    report = collect(list(cfg["models"]))
    report["metadata"] = run_metadata()
    save_json(report, RESULTS_DIR / "benchmark_report.json")
    md = to_markdown(report)
    (RESULTS_DIR / "benchmark_report.md").write_text(md, encoding="utf-8")
    print(f"wrote {RESULTS_DIR / 'benchmark_report.md'}")


if __name__ == "__main__":
    main()
